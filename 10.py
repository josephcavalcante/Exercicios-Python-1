# 10.Faça um programa que receba uma variável int e outra float. Em seguida,
# converta ambas para string e imprima as mesmas.

num_int = int(input("Digite um número inteiro: "))
num_float = float(input("Digite um número decimal: "))
num_int_str = str(num_int)
num_float_str = str(num_float)
print("Número inteiro como string:", num_int_str)
print("Número decimal como string:", num_float_str)